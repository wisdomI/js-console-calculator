let opr = prompt("operator: ");
let num1 = parseInt(prompt("number 1: "));
let num2 = parseInt(prompt("number 2: "));

if (opr = "+") {
    console.log(num1 + num2);    
}   else if(opr = "-") 
    console.log(num1 - num2);
    else if (opr = "*") {
        console.log(num1 * num2);
    } 
    else if ("/") {
        console.log(num1 / num2);
    }
    else {
    console.log("no input")
}



